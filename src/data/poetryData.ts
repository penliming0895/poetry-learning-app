export const getPoetryById = (id: string): Poetry | undefined => {
  return poetryDatabase.find(p => p.id === id);
};

export const getPoetryByDifficulty = (difficulty: 'easy' | 'medium' | 'hard'): Poetry[] => {
  return poetryDatabase.filter(p => p.difficulty === difficulty);
};

export const getRandomPoetry = (count: number = 1): Poetry[] => {
  const shuffled = [...poetryDatabase].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};
